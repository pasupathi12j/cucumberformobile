package com.qa.utils;

public class CustomData {

    protected String message;
    protected String platformName;
    protected String viewAttribute;
    protected String staticAttribute;
    protected String buttonAttribute;
    protected String textAttribute;
    protected String imageAttribute;
    protected String attribute;


    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getPlatformName() {
        return platformName;
    }

    public void setPlatformName(String platformName) {
        this.platformName = platformName;
    }

    public String getViewAttribute() {
        return viewAttribute;
    }

    public void setViewAttribute(String viewAttribute) {
        this.viewAttribute = viewAttribute;
    }

    public String getButtonAttribute() {
        return buttonAttribute;
    }

    public void setButtonAttribute(String buttonAttribute) {
        this.buttonAttribute = buttonAttribute;
    }

    public String getTextAttribute() {
        return textAttribute;
    }

    public void setTextAttribute(String textAttribute) {
        this.textAttribute = textAttribute;
    }

    public String getImageAttribute() {
        return imageAttribute;
    }

    public void setImageAttribute(String imageAttribute) {
        this.imageAttribute = imageAttribute;
    }

    public String getAttribute() {
        return attribute;
    }

    public void setAttribute(String attribute) {
        this.attribute = attribute;
    }

    public String getStaticAttribute() {
        return staticAttribute;
    }

    public void setStaticAttribute(String staticAttribute) {
        this.staticAttribute = staticAttribute;
    }
}
